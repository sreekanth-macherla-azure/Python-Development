# Float Variables
num1 = 6.0
num2 = 3.5

#Basic Arithmetics
result1 = num1 + num2
print("Addition:", result1)

result2 = num1 - num2
print("Substraction:", result2)

result3 = num1 * num2
print("multiplication:", result3)

result4 = num1 / num2
print("Division:", result4)

result5 = round(3.4, 2)
print("Rounded:", result5)
